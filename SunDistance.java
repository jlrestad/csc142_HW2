package space;

//planet distances from the sun
public class SunDistance {  
	private final double MERCURY = 5.79*Math.pow(10, 10);
	private final double VENUS = 1.08*Math.pow(10, 11);
	private final double EARTH = 1.50*Math.pow(10, 11);
	private final double MARS = 2.28*Math.pow(10, 11);
	private final double JUPITER = 7.78*Math.pow(10, 11);
	private final double SATURN = 1.43*Math.pow(10, 12);
	private final double URANUS = 2.87*Math.pow(10, 12);
	private final double NEPTUNE = 4.50*Math.pow(10,12);
	
	
	public double mercury() {
		return MERCURY;
	}
	
	public double venus() {
		return VENUS;
	}
	
	public double earth() {
		return EARTH;
	}
	
	public double mars() {
		return MARS;
	}
	
	public double jupiter() {
		return JUPITER;
	}
	
	public double saturn() {
		return SATURN;
	}
	
	public double uranus() {
		return URANUS;
	}
	
	public double neptune() {
		return NEPTUNE;
	}
		

	
}
