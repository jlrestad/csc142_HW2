package space;

public class Mass {
	
	public final double MERCURY = 3.18 * Math.pow(10,23);
	public final double VENUS = 4.88 * Math.pow(10,24);
	public final double EARTH = 5.98 * Math.pow(10,24);
	public final double MARS = 6.42 * Math.pow(10,23);
	public final double JUPITER = 1.90 * Math.pow(10,27);
	public final double SATURN = 5.68 * Math.pow(10,25);
	public final double URANUS = 8.68 * Math.pow(10,25);
	public final double NEPTUNE = 1.03 * Math.pow(10,26);
	
		
	public double getMassMercury() {
		return MERCURY;
	}
	
	public double getMassVenus() {
		return VENUS;
	}
	
	public double getMassEarth() {
		return EARTH;
	}
	
	public double getMassMars() {
		return MARS;
	}
	
	public double getMassJupiter() {
		return JUPITER;
	}
	
	public double getMassSaturn() {
		return SATURN;
	}
	
	public double getMassUranus() {
		return URANUS;
	}
	
	public double getMassNeptune() {
		return NEPTUNE;
	}
	

}
